/*************************************************************************************************
*****
***** 函数名:void  Crc16()
*****
***** 入口参数：ptr--输入数据缓冲；count--输入字节的个数；
*****
***** 功能描述：计算crc的校验码
*****
***** 调用： 无
*****
***** 返回值：
*****
***** 作者:Sandy
****	
****  版本：v2.0
****
**************************************************************************************************/
int Crc16(uchar *ptr, int count)
{
	int crc, i;
	crc = 0;
	while (--count >= 0){
		crc = (crc ^ (((int)*ptr++) << 8));
		for (i = 0; i < 8; ++i)
			if (crc & 0x8000)
				crc = ((crc << 1) ^ 0x1021);
			else
				crc = crc << 1;
	}
	return (crc & 0xFFFF);
}

